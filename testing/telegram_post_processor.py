class TelegramPostProcessor:
    def __init__(self, api_key: str):
        self.api_key = api_key
        openai.api_key = api_key
        self.posts = []
        self.style_analysis = {}
    
    def load_posts(self, filename: str):
        with open(filename, 'r', encoding='utf-8') as f:
            self.posts = json.load(f)
        print(f"Loaded {len(self.posts)} posts")
        self.analyze_posts()
    
    def analyze_posts(self):
        self.style_analysis = {
            'hashtag_posts': [],
            'fact_posts': [],
            'product_posts': [],
            'seasonal_posts': [],
            'personal_posts': [],
            'educational_posts': [],
            'promo_posts': [],
            'inspiration_posts': []
        }
        
        for post in self.posts:
            if '#' in post:
                self.style_analysis['hashtag_posts'].append(post)
            if '–ò–Ω—Ç–µ—Ä–µ—Å–Ω—ã–µ –§–∞–∫—Ç—ã' in post:
                self.style_analysis['fact_posts'].append(post)
            if any(word in post for word in ['—Å–≤–µ—á–∞', '–∞—Ä–æ–º–∞–¥–∏—Ñ—Ñ—É–∑–æ—Ä', '–≤—ã–ø–æ–ª–Ω–µ–Ω', '–æ—Ç—Ç–µ–Ω–æ–∫']):
                self.style_analysis['product_posts'].append(post)
            if any(word in post for word in ['–ù–æ–≤—ã–π –≥–æ–¥', '–≤–µ—Å–Ω–∞', '–ª–µ—Ç–æ', '–æ—Å–µ–Ω—å', '–∑–∏–º–∞', '8 –º–∞—Ä—Ç–∞', '–†–æ–∂–¥–µ—Å—Ç–≤–æ']):
                self.style_analysis['seasonal_posts'].append(post)
            if any(word in post for word in ['—è ', '–º–æ–π', '–º–æ—è', '–º–µ–Ω—è', '–º–Ω–µ']):
                self.style_analysis['personal_posts'].append(post)
            if any(word in post for word in ['–∑–Ω–∞–µ—Ç–µ', '—Ñ–∞–∫—Ç', '–∏—Å—Ç–æ—Ä–∏—è', '—Ç—Ä–∞–¥–∏—Ü']):
                self.style_analysis['educational_posts'].append(post)
            if any(word in post for word in ['–∑–∞–∫–∞–∑', '—Ü–µ–Ω–∞', '—Å—Ç–æ–∏–º–æ—Å—Ç—å', '—Å–∫–∏–¥–∫', '–ø–æ–¥–∞—Ä–æ–∫']):
                self.style_analysis['promo_posts'].append(post)
            if any(word in post for word in ['–≤–¥–æ—Ö–Ω–æ–≤–µ–Ω', '–Ω–∞—Å—Ç—Ä–æ–µ–Ω', '–∞—Ç–º–æ—Å—Ñ–µ—Ä', '—É—é—Ç']):
                self.style_analysis['inspiration_posts'].append(post)
    
