    if not api_key:
        raise ValueError(
            "OPENAI_API_KEY environment variable is not set. "
            "Please set it in your .env file or as an environment variable."
        )
    
    processor = TelegramPostProcessor(api_key=api_key)
    processor.load_posts("messages_simple_list.json")
    
    print("\nGenerating training examples...")
    examples = processor.generate_training_examples()
    
    processor.validate_examples(examples)
    
    output_file = "telegram_posts_training.jsonl"
