    def save_to_jsonl(self, examples: List[Dict[str, Any]], output_file: str):
        with open(output_file, 'w', encoding='utf-8') as f:
            for example in examples:
                f.write(json.dumps(example, ensure_ascii=False) + '\n')
        print(f"Saved {len(examples)} training examples to {output_file}")
    
    def validate_examples(self, examples: List[Dict[str, Any]]):
        print("\nValidation Report:")
        print(f"Total examples: {len(examples)}")
        
        valid_count = 0
        for i, example in enumerate(examples):
            if "messages" in example and len(example["messages"]) == 3:
                if all(msg.get("role") in ["system", "user", "assistant"] for msg in example["messages"]):
                    valid_count += 1
                else:
                    print(f"Invalid roles in example {i}")
            else:
                print(f"Invalid structure in example {i}")
        
        print(f"Valid examples: {valid_count}/{len(examples)}")
        
        user_lengths = [len(ex["messages"][1]["content"]) for ex in examples if len(ex.get("messages", [])) > 1]
        assistant_lengths = [len(ex["messages"][2]["content"]) for ex in examples if len(ex.get("messages", [])) > 2]
        
        print(f"\nUser prompt lengths: min={min(user_lengths)}, max={max(user_lengths)}, avg={sum(user_lengths)//len(user_lengths)}")
        print(f"Assistant response lengths: min={min(assistant_lengths)}, max={max(assistant_lengths)}, avg={sum(assistant_lengths)//len(assistant_lengths)}")

